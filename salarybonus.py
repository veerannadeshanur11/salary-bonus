# Salary Bonus Calculator Program (with 10% bonus)

salary = float(input("Enter the employee's salary: "))

bonus_salary = salary + (salary * 0.10)

print("\nOriginal Salary:", int(salary))
print("Bonus Added:", int(bonus_salary))